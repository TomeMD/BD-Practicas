CREATE TABLE PACIENTE
(	DNI VARCHAR(9),
	NomePila VARCHAR(15) CONSTRAINT NN_PACIENTE_NomePila NOT NULL,
	AP1 VARCHAR(15),
	AP2 VARCHAR(15),
	MetodoRexistro VARCHAR(35),
	CONSTRAINT PACIENTE_KEY PRIMARY KEY (DNI)
);

CREATE TABLE TELEFONOS_PACIENTE
(	DNI VARCHAR(9),
	Telefono NUMERIC(9, 0),
	CONSTRAINT TELEFONOS_PACIENTE_KEY PRIMARY KEY (DNI, Telefono)
);

CREATE TABLE CENTRO 
(	CodCentro VARCHAR(3),
	Tipo VARCHAR(20),
	Nome VARCHAR(35),
	Enderezo VARCHAR(35) NOT NULL,
	CONSTRAINT CENTRO_KEY PRIMARY KEY (CodCentro)
);

CREATE TABLE EQUIPO
(	CodEquipo NUMERIC(4, 0),
	CentroAsignado VARCHAR(3),
	CONSTRAINT EQUIPO_KEY PRIMARY KEY (CodEquipo)
);

CREATE TABLE INGRESO
(	DNI VARCHAR(9),
	DataHoraInicio DATE,
	DataHoraFin DATE,
	CodCentro VARCHAR(3) NOT NULL,
	Motivo VARCHAR(110),
	CONSTRAINT INGRESO_KEY PRIMARY KEY (DNI, DataHoraInicio)
);

CREATE TABLE SANITARIO
(	NSS NUMERIC(11, 0),
	NomePila VARCHAR(15) CONSTRAINT NN_SANITARIO_NomePila NOT NULL,
	AP1 VARCHAR(15),
	AP2 VARCHAR(15),
	Posto VARCHAR(20),
	CodCentro VARCHAR(3),
	CONSTRAINT SANITARIO_KEY PRIMARY KEY (NSS)
);

CREATE TABLE TRATAMENTO
(	DNI VARCHAR(9),
	DataHoraInicio DATE,
	DataHoraFin DATE,
	NSS NUMERIC(11, 0) NOT NULL,
	CONSTRAINT TRATAMENTO_KEY PRIMARY KEY (DNI, DataHoraInicio)
);

CREATE TABLE REVISION
(	DNI VARCHAR(9),
	DataHora DATE,
	Descricion VARCHAR(50),
	Metodo VARCHAR(20) NOT NULL,
	CONSTRAINT REVISION_KEY PRIMARY KEY (DNI, DataHora)
);

CREATE TABLE EXPLORACION
(	CodExploracion VARCHAR(3),
	Tipo VARCHAR(20),
	CONSTRAINT EXPLORACION_KEY PRIMARY KEY (CodExploracion)
);

CREATE TABLE MEDICAMENTO
(	CodMedicamento NUMERIC(4, 0),
	Nome VARCHAR(20) NOT NULL,
	CONSTRAINT MEDICAMENTO_KEY PRIMARY KEY (CodMedicamento)
);

CREATE TABLE RECOLLE
(	DNI VARCHAR(9),
	DataHora DATE,
	CodExploracion VARCHAR(3),
	NSS NUMERIC(11, 0),
	Resultado NUMERIC(8, 1),
	Descricion VARCHAR(35),
	CONSTRAINT RECOLLE_KEY PRIMARY KEY (DNI, DataHora, CodExploracion)
);

CREATE TABLE SUMINISTRA
(	DNI VARCHAR(9),
	DataHoraInicio DATE,
	CodMedicamento NUMERIC(4, 0),
	Dose VARCHAR(15),
	Duracion NUMERIC(2, 0),
	CONSTRAINT SUMINISTRA_KEY PRIMARY KEY (DNI, DataHoraInicio, CodMedicamento)
);

CREATE TABLE TIPO_MATERIAL
(	CodMaterial VARCHAR(3),
	Nome VARCHAR(15) NOT NULL,
	CONSTRAINT TIPO_MATERIAL_KEY PRIMARY KEY (CodMaterial)
);

CREATE TABLE USA
(	CodMaterial VARCHAR(3),
	CodCentro VARCHAR(3),
	Stock NUMERIC(5, 0),
	StockMinimo NUMERIC(5, 0),
	CONSTRAINT USA_KEY PRIMARY KEY (CodMaterial, CodCentro)
);

CREATE TABLE QUENDA
(	CodEquipo NUMERIC(4, 0),
	DataHoraInicio DATE,
	DataHoraFin DATE,
	CodCentro VARCHAR(3) NOT NULL,
	CONSTRAINT QUENDA_KEY PRIMARY KEY (CodEquipo, DataHoraInicio)
);

CREATE TABLE UNIDADE
(	Referencia NUMERIC(10, 0),
	CodMaterial VARCHAR(3) NOT NULL,
	CodEquipo NUMERIC(4, 0),
	DataHoraInicio DATE,
	CONSTRAINT UNIDADE_KEY PRIMARY KEY (Referencia)
);


CREATE TABLE ENTRA_EN
(	CodEquipo NUMERIC(4, 0) NOT NULL,
	NSS NUMERIC(11, 0),
	DataHoraEntrada DATE,
	DataHoraSaida DATE,
	CONSTRAINT ENTRA_EN_KEY PRIMARY KEY (NSS, DataHoraEntrada)
);



